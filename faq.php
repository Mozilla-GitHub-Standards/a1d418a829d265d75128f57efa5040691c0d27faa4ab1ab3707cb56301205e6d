<div id="reponse"><h2> Réponses :</h2>
		<dl>
		    <dt id="prix"> Pourquoi n'est-ce pas cher ?</dt> 
		        <dd> 
Le système est relativement moins gourmand que ses homologues et il n'y a pas besoin de posséder un téléphone très puissant. </dd>
		    <dt id="candycrush"> Est-ce qu'il y a Candy Crush ?</dt> 
		        <dd>Oui, il est disponible sur le Firefox Marketplace. Attention, ce n'est pas le jeu complet avec les missions et tout. Juste le système de jeux.</dd>
		    <dt id="fb"> Est-ce qu'il y a Facebook ?</dt> 
		        <dd> Oui, il est pré-embarqué.</dd>
		    <dt id="tw"> Est-ce qu'il y a Twitter ?</dt> 
		        <dd> Oui, il est pré-embarqué.</dd>
		    <dt id="whatapp"> Est-ce qu'il y a Whatsapp ?</dt> 
		        <dd>
PLusieurs applications permettent de se connecter à Whatsapp, telles que ConnectA2 et Loqui IM . Ce sont des agrégateurs de messagerie instantanée, on peut récupérer tous ses contacts Whatsapp et chatter avec eux, en passant par la plateforme Whatsapp.</dd>
		    <dt id="snapchat">Est-ce qu'il y a Snapchat ?</dt> 
		        <dd>Non, pas encore, mais ça ne saurait tarder.</dd>
		    <dt id="mail"> Est-ce que je peux consulter mes emails avec ça ?</dt> 
				<dd> Oui, il est très facile de récupérer ses mails via l'application dédiée (les protocoles POP3 et IMAP sont supportés)</dd>
		    <dt id="gps"> Est-ce qu'il peut remplacer mon GPS ?</dt> 
				<dd>Avec l'application HERE Maps ou via le site de Google Maps, oui</dd>
		    <dt id="migration"> Est-ce que je peux migrer mes données de mon ancien téléphone ? (Android/iOS)</dt> 
					<dd> Oui, vous pouvez synchroniser vos contacts avec Facebook, Google ou les importer de la carte SIM/microSD.
Vous pouvez synchroniser votre agenda avec Google ou via CalDAV.
Le reste des données peut être copié sur le téléphone facilement (images, vidéos, musique).</dd>
		    <dt id="stockage"> Il n'y a pas beaucoup de place (1 Go). Il y en a plus sur Android ou iOS (iPhone).</dt> 
		        <dd>Oui mais on peut ajouter une microSD pour stocker, les vidéos, photos. Et les applis prennent beaucoup moins de place. La taille maximale de la carte microSD qu'on peut insérer est de 32 Go. Et l'ajout de stockage en achetant une nouvelle carte microSD revient moins cher que d'acheter un stockage défini au départ que l'on ne peut pas changer, sans changer de téléphone.
</dd>
		    <dt id="appandroios"> Est ce que je peux installer des applications Android/iOS ?</dt> 
		        <dd> Non, ces applications ne sont pas installables, cependant il est possible de consulter la version web de ces applications si elle existe.</dd>
		    <dt id="musique"> Est-ce que je peux mettre ma musique ?</dt> 
		        <dd>Oui, Firefox OS lit les principaux formats de musique (AAC, MP3, OGG…)</dd>
		    <dt id="photo"> Est-ce que je peux faire des photos ?</dt> 
		        <dd>Oui tout à fait, il y a un appareil photo (et même des filtres !)</dd>
		    <dt id="maj"> Est-ce que le téléphone sera mis à jour régulièrement ?</dt> 
					<dd>ZTE proposera les mises à jour.</dd>
		    <dt id="pourquoi">  Pourquoi Firefox OS ?</dt> 
		        <dd>Il s'agit de proposer une plate-forme plus simple, plus ouverte et respectueuse des utilisateurs. Basée sur des standards utilisés par tous, ceux du web.</dd>
		    <dt id="compte"> Est-ce qu'il faut un compte ?</dt> 
		        <dd>Non, il n'y a pas besoin de compte pour utiliser le téléphone et installer des applications toutefois en ajouter un permet certaines choses comme noter et commenter les applications du Marketplace.</dd>
		    <dt id="apps"> Où peut-on trouver les applications ?</dt>
				<dd> Sur le Marketplace (<a href="https://marketplace.firefox.com/">
https://marketplace.firefox.com/</a>) et sur certains sites qui proposent des applications sans passer par le marketplace.
On peut aussi faire une «&nbsp;recherche intelligente&nbsp;»&nbsp;: des applications, webapps ainsi que des sites web sont directement accessibles (pas besoin de télécharger et installer pour utiliser). De plus, quand on ouvre le lien, on arrive directement sur le sujet qu'on recherchait à l'intérieur de l'application.</dd>
		    <dt id="sim"> Quelle taille de carte SIM ?</dt> 
		        <dd>C'est une carte SIM normale, telle que fournie par votre opérateur. Ce n'est pas une nano-SIM comme dans l'iPhone 5/5S/6.</dd>
		    <dt id="kesako"> C'est quoi exactement, qu'est-ce-que je peux faire avec ?</dt> 
		        <dd> C'est un smartphone qui vous permet de téléphoner (heureusement), envoyer des SMS/MMS, prendre des photos, gérer votre agenda, vos contacts, écouter la radio/musique, aller sur Internet, envoyer/recevoir des mails et plus encore ! Il a tout ce qu'on peut attendre d'un smartphone, une interface simple et lisible en plus !</dd>
		    <dt id="help"> Si je comprends rien quelqu'un peut m'aider ?</dt> 
		        <dd>En français :<ul>
    			<li><a href="http://forums.mozfr.org">http://forums.mozfr.org</a></li>
    			<li><a href="https://support.mozilla.org/fr/products/firefox-os">https://support.mozilla.org/fr/products/firefox-os</a></li>
				<li> irc.mozilla.org #support-fr </li> </ul></dd>
		    <dt id="reactif"> Quelqu'un sait-il ce que ça donne en termes de réactivité ?</dt> 
				<dd>Il est réactif et fluide, bien + "léger" qu'un Android. Il demande moins de ressources aussi</dd>
		    <dt id="batterie"> La batterie dure longtemps ?</dt> 
		        <dd>Tout dépend de l'utilisation, mais deux voir trois journées sont possibles environ.</dd>
		    <dt id="androidpascher"> Il existe des versions d'Android pour les téléphones bon marché... En quoi Firefox OS se démarque-t-il ?</dt> 
				<dd>Il se démarque par sa fludité, pas de problème d'applications qui se désinstalle mal ou de ralentissement au bout de 6 mois.</dd>
		    <dt id="pourquoilui"> Pourquoi choisir ce téléphone ?</dt> 
				<dd>Firefox OS est simple comme bonjour, l'interface est intuitive et simple&nbsp;: tout est disponible dès le démarrage du téléphone.</dd>
		    <dt id="bluetooth"> Il a le Bluetooth ?</dt> 
		        <dd>Oui le ZTE Open C est équipé du Bluetooth 4.0. </dd>
		    <dt id="sav"> Comment marche le SAV ?</dt> 
		        <dd>Le SAV est assuré par ZTE France.</dd>
		    <dt id="langues"> Quelles sont les langues disponibles sur le ZTE Open C ?</dt> 
		        <dd>Du français à l'anglais en passant par le catalan plus de 25 langues sont disponibles.</dd>
		    <dt id="radio"> Possède-t-il la radio ?</dt>
					<dd>Oui il possède un module de radio FM. Toutefois des écouteurs sont nécessaires pour faire office d'antenne.</dd>
		    <dt id="bidouille"> Où puis-je trouver des informations sur le root et la bidouille du téléphone ?</dt> 
				<dd>Vous trouverez de nombreuses et utiles informations sur le <a href="https://developer.mozilla.org/fr/Firefox_OS/Guide_Telephone_Developpeur/ZTE_OPEN_C">Mozilla Developer Network </a></dd>
		    <dt id="dev"> Ce ZTE Open C est-il suffisant pour pouvoir développer des applis, modifier la home, etc. ou il faut forcément acheter un Flame ?</dt> 
				<dd>ZTE Open C c'est bien, Flame c'est mieux : <a href="https://wiki.mozilla.org/FirefoxOS/Flame#FAQs">https://wiki.mozilla.org/FirefoxOS/Flame#FAQs </a>
Toutefois, si vous avez un petit budget l'Open C est suffisant. ZTE fournit un logiciel qui permet de rooter son Open C et le mettre à jour avec une ROM développeur.</dd>
		    <dt id="majhomemade"> Comment puis-je mettre à jour moi-même Firefox OS vers la dernière version ou vers une ROM développeur?</dt> 						<dd>Des informations sont disponibles <a href="https://developer.mozilla.org/fr/Firefox_OS/Guide_Telephone_Developpeur/ZTE_OPEN_C" >ici</a>. ZTE fournit aussi un outil de root et de mise à jour vers une ROM développeur <a href="http://www.ztefrance.com/downloads/Pack_root_du_ZTE_Open_C.zip"> ici. </a></dd>
		    <dt id="4g"> Sait-on quand arrivera la prise en charge de la 4G par Firefox OS ? et quand des téléphones compatibles 4G apparaîtront ?</dt>
				<dd>Non, on ne sait pas. Ce qui est sûr, c'est que l'Open C n'intègre pas la 4G. LTE est intégré dans la version 1.4. cf. https://wiki.mozilla.org/B2G/Roadmap/Archive_-_Past_Releases_Complete_Features
Au MWC2014, Alcatel avait fait une annonce pour l'Alcatel OneTouch Fire S prévu pour décembre 2014 : http://www.phonearena.com/phones/Alcatel-OneTouch-Fire-S_id8451 </dd>
		</dl>
